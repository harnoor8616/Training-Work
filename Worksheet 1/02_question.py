# 2. Input two numbers and print their addition, subtraction, and multiplication.
number1 = float(input("Please enter the first number: "))
number2 = float(input("Please enter the second number: "))  
print(f"The addition of {number1} and {number2} is {number1 + number2}.")
print(f"The subtraction of {number1} from {number2} is {number2 - number1}.")
print(f"The multiplication of {number1} and {number2} is {number1 * number2}.")         
